class Solution {
    public int minEatingSpeed(int[] piles, int h) {
        int left = 1;
        int right = 0;
        for(int p : piles){
            right = Math.max(right,p);
        }
        int result = right;
        while(left <= right){
            int mid = left + (right - left)/2;
            long hours = totalhours(piles,mid);
            if(hours <= h){
                result = mid;
                right = mid-1;
            }
            else{
                left = mid+1;
            }
        }
        return result;
    }
    public long totalhours(int piles[],int k){
        long hours = 0;
        for(int p : piles){
            hours += (int)Math.ceil((double)p/k);
        }
        return hours;
    }
}